package com.scriptico.beaconlocator.data.dto;

/**
 * 
 * @author Cyril Deba
 * @version 1.0
 * 
 */
public class MediaContentHolderDto extends ContentHolderDto {

    private static final long serialVersionUID = 1L;

    private String name;

    @Override
    public String toString() {
	return name;
    }

    @Override
    public int hashCode() {
	return super.hashCode();
    }

    @Override
    public boolean equals(Object obj) {
	if (this == obj)
	    return true;
	if (obj == null)
	    return false;
	if (getClass() != obj.getClass())
	    return false;
	MediaContentHolderDto other = (MediaContentHolderDto) obj;
	if (name == null) {
	    if (other.name != null)
		return false;
	} else if (!name.equals(other.name))
	    return false;
	return true;
    }

    /**
     * @return the name
     */
    public String getName() {
	return name;
    }

    /**
     * @param name
     *            the name to set
     */
    public void setName(String name) {
	this.name = name;
    }
}
